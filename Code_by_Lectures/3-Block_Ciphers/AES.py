# ---------------------
# RCON (Round constants)
# ---------------------
RCON = [0x00,0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1B,0x36]

# ============================================================
#                   HELPER FUNCTIONS
# ============================================================

def sub_bytes(state):
    new = []
    for b in state:
        new.append(SBOX[b])
    return new

def shift_rows(state):
    new = state[:]

    # Row 1
    new[1]  = state[5]
    new[5]  = state[9]
    new[9]  = state[13]
    new[13] = state[1]

    # Row 2
    new[2]  = state[10]
    new[6]  = state[14]
    new[10] = state[2]
    new[14] = state[6]

    # Row 3
    new[3]  = state[15]
    new[7]  = state[3]
    new[11] = state[7]
    new[15] = state[11]

    return new

def xtime(a):
    a = a << 1
    if a & 0x100:
        a ^= 0x1B
    return a & 0xFF

def mix_single_column(col):
    c0, c1, c2, c3 = col[0], col[1], col[2], col[3]

    t0 = xtime(c0) ^ (xtime(c1) ^ c1) ^ c2 ^ c3
    t1 = c0 ^ xtime(c1) ^ (xtime(c2) ^ c2) ^ c3
    t2 = c0 ^ c1 ^ xtime(c2) ^ (xtime(c3) ^ c3)
    t3 = (xtime(c0) ^ c0) ^ c1 ^ c2 ^ xtime(c3)

    return [t0 & 0xFF, t1 & 0xFF, t2 & 0xFF, t3 & 0xFF]

def mix_columns(state):
    new = state[:]
    for col in range(4):
        idx = col * 4
        column = [state[idx], state[idx+1], state[idx+2], state[idx+3]]
        mixed = mix_single_column(column)
        new[idx]   = mixed[0]
        new[idx+1] = mixed[1]
        new[idx+2] = mixed[2]
        new[idx+3] = mixed[3]
    return new

def add_round_key(state, key):
    result = []
    for i in range(16):
        result.append(state[i] ^ key[i])
    return result

# ============================================================
#                   KEY SCHEDULE
# ============================================================

def rot_word(w):
    return [w[1], w[2], w[3], w[0]]

def sub_word(w):
    nw = []
    for b in w:
        nw.append(SBOX[b])
    return nw

def key_expansion(key):
    # AES-128 â†’ 44 words
    words = []
    i = 0

    # First 4 words = original key
    while i < 16:
        words.append([key[i], key[i+1], key[i+2], key[i+3]])
        i += 4

    # Expand to 44 words
    for r in range(4, 44):
        temp = words[r-1][:]

        if r % 4 == 0:
            temp = rot_word(temp)
            temp = sub_word(temp)
            temp[0] ^= RCON[r // 4]

        w = []
        for j in range(4):
            w.append(words[r-4][j] ^ temp[j])

        words.append(w)

    # Convert to round keys (each 16 bytes)
    round_keys = []
    for r in range(11):
        rk = []
        for w in range(4):
            rk.extend(words[r*4 + w])
        round_keys.append(rk)

    return round_keys

# ============================================================
#                   AES-128 ENCRYPTION
# ============================================================

def aes_encrypt(plaintext, key):
    # plaintext, key are lists of 16 bytes
    round_keys = key_expansion(key)

    state = add_round_key(plaintext, round_keys[0])

    for r in range(1, 10):  # rounds 1-9
        state = sub_bytes(state)
        state = shift_rows(state)
        state = mix_columns(state)
        state = add_round_key(state, round_keys[r])

    # Final round (no MixColumns)
    state = sub_bytes(state)
    state = shift_rows(state)
    state = add_round_key(state, round_keys[10])

    return state


